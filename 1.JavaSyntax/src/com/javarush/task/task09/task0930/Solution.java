public class Solution {
    public static void main(String[] args) throws Exception {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        ArrayList<String> list = new ArrayList<>();
        while (true) {
            String s = reader.readLine();
            if (s.isEmpty()) {
                break;
            }
            list.add(s);
        }

        String[] array = list.toArray(new String[0]);
        sort(array);

        // for (String x : array) {
        //     System.out.println(x);
        // }
    }

    public static void sort(String[] array) {
        ArrayList<Integer> theNumbers = new ArrayList<>();
        ArrayList<String> theStrings = new ArrayList<>();
        for(int i = 0;i<array.length;i++){
            String s = array[i];
            if (isNumber(s)){
                theNumbers.add(Integer.parseInt(s));
            }
            else theStrings.add(s);
        }
        theNumbers.sort(Collections.reverseOrder());
        //Collections.sort(theNumbers);
        Collections.sort(theStrings);
        //theNumbers.sort();

        //theStrings.sort();
        boolean f = isGreaterThan("a","b");
         for (Integer x : theNumbers) {
             System.out.println(x);
         }
         for (String x : theStrings) {
             System.out.println(x);
         }
        
    }

    // Метод для сравнения строк: 'а' больше чем 'b'
    public static boolean isGreaterThan(String a, String b) {
        return a.compareTo(b) > 0;
    }


    // Переданная строка - это число?
    public static boolean isNumber(String s) {
        if (s.length() == 0) {
            return false;
        }

        char[] chars = s.toCharArray();
        for (int i = 0; i < chars.length; i++) {
            char c = chars[i];
            if ((i != 0 && c == '-') // Строка содержит '-'
                    || (!Character.isDigit(c) && c != '-') // или не цифра и не начинается с '-'
                    || (chars.length == 1 && c == '-')) // или одиночный '-'
            {
                return false;
            }
        }
        return true;
    }
}
